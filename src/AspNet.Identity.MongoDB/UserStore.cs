    public class UserStore : UserStore<IdentityUser>
    {
        public UserStore(IdentityContextBase context, IdentityErrorDescriber describer = null) : base(context, describer) { }
    }

    public class UserStore<TUser> : UserStore<TUser, IdentityContextBase>
        where TUser : IdentityUser
    {
        public UserStore(IdentityContextBase context, IdentityErrorDescriber describer = null) : base(context, describer) { }
    }

    public class UserStore<TUser, TContext> :
        IUserLoginStore<TUser>,
        IUserRoleStore<TUser>,
        IUserClaimStore<TUser>,
        IUserPasswordStore<TUser>,
        IUserSecurityStampStore<TUser>,
        IUserEmailStore<TUser>,
        IUserLockoutStore<TUser>,
        IUserPhoneNumberStore<TUser>,
        IQueryableUserStore<TUser>,
        IUserTwoFactorStore<TUser>
        where TUser : IdentityUser
        where TContext : IdentityContextBase
    {
        public UserStore(TContext context, IdentityErrorDescriber describer = null)
        {
            if (context == null)
            {
                throw new ArgumentNullException(nameof(context));
            }
            Context = context;
            ErrorDescriber = describer ?? new IdentityErrorDescriber();
        }

        public IQueryable<TUser> Users
        {
            get
            {
                return Context.GetUsers<TUser>().AsQueryable();
            }
        }

        public TContext Context { get; private set; }

        public IdentityErrorDescriber ErrorDescriber { get; set; }

